package vazkii.neat.mixin.plugin;

import com.falsepattern.lib.mixin.IMixin;
import com.falsepattern.lib.mixin.ITargetedMod;
import lombok.Getter;
import lombok.RequiredArgsConstructor;

import java.util.List;
import java.util.function.Predicate;

import static com.falsepattern.lib.mixin.IMixin.PredicateHelpers.always;
import static com.falsepattern.lib.mixin.IMixin.PredicateHelpers.require;

@RequiredArgsConstructor
public enum Mixin implements IMixin {
    RendererLivingEntityMixin(Side.CLIENT, always(), "RendererLivingEntityMixin"),
    compat_ntm_RenderOverheadMixin(Side.CLIENT, require(TargetedMod.HBM_NTM), "compat.ntm.RenderOverheadMixin"),
    ;

    @Getter
    private final Side side;
    @Getter
    private final Predicate<List<ITargetedMod>> filter;
    @Getter
    private final String mixin;
}

